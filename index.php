<script>
  document.addEventListener("DOMContentLoaded", function () {
    const startedAtField = document.getElementById("form_started_at");
    if (startedAtField) {
      startedAtField.value = Date.now();
    }

    function scrollWithOffset(targetId) {
      const target = document.getElementById(targetId);
      const header = document.getElementById("header");

      if (!target) return;

      const headerHeight = header ? header.offsetHeight : 0;
      const extraOffset = 20;
      const targetPosition =
        target.getBoundingClientRect().top + window.pageYOffset - headerHeight - extraOffset;

      window.scrollTo({
        top: targetPosition,
        behavior: "smooth"
      });
    }

    document.querySelectorAll('a[href*="#"]').forEach(link => {
      link.addEventListener("click", function (event) {
        const href = this.getAttribute("href");
        if (!href) return;

        const parts = href.split("#");
        const hash = parts[1];

        if (!hash) return;

        const path = parts[0];
        const currentPage = window.location.pathname.split("/").pop() || "index.php";

        const isSamePage =
          path === "" ||
          path === "#" ||
          path === currentPage ||
          path === "index.php";

        if (isSamePage) {
          event.preventDefault();
          scrollWithOffset(hash);
          history.replaceState(null, "", "#" + hash);
        }
      });
    });

    if (window.location.hash) {
      const hash = window.location.hash.substring(1);

      setTimeout(() => {
        scrollWithOffset(hash);
      }, 200);
    }
  });
</script>

